//Incrementos o decrementos

let puntaje = 5;

//incremento de  en 
puntaje++; //retorna 5 inicialmente hasta que se vuelva a llamar
++puntaje; //retorna 6 de inmediato

//decremento de  1 en 1
puntaje--; //retorna 5 inicialmente hasta que se vuelva a llamar
--puntaje; //retorna inmediatamente 

//incremento en valores diferentes a 1
puntaje += 3; //retorna 8
puntaje += 3; //retorna 11
puntaje += 3; //retorna 14

//decremento en valores diferentes a 1
puntaje -= 3; //retorna 11
puntaje -= 3; //retorna 8
puntaje -= 3; //retorna 5


console.log(puntaje);